import mongoose from "mongoose";


const Schema = mongoose.Schema;

const promotionSchema = new Schema({
    name: {
        type: String,
        required: true
    },
    image: {
        type: String,
        required: true
    },
    label: {
        type: String,
        default: ''
    },
    price: {
        type: String,
        required: true,
        min: 0
    },
    description: {
        type: String,
        required: true
    },
    featured: {
        type: Boolean,
        default: false
    },
});

const Promotion = mongoose.model('Promotion', promotionSchema);
export default Promotion;       